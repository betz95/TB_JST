    private void openDataSetButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_openDataSetButtonMousePressed
        jf = new JFileChooser(new File("C:/"));
        jf.setFileFilter(new FileNameExtensionFilter("CSV files", "csv"));
        jf.setDialogTitle("Choose CSV File");
        int feedback = jf.showOpenDialog(this);
        final String inputURI = jf.getSelectedFile().getAbsolutePath();
        if(feedback == JFileChooser.APPROVE_OPTION){
            File selectedFile = jf.getSelectedFile();
            try{
                BufferedReader br = new BufferedReader(new FileReader(selectedFile));
                String buffer;
                String buffers[];
                while((buffer=br.readLine())!=null){
                    buffer = buffer.toLowerCase();
                    buffers = buffer.split(";");
                    String mNama = buffers[0];
                    int mGender = hmGender.get(buffers[1]);
                    int mJurusan = hmJurusan.get(buffers[2]);
                    int mHobi = hmHobi.get(buffers[3]);
                    int mKotaAsal = hmKotaAsal.get(buffers[4]);
                    int mFrekuensiAkses = hmFrekuensiAkses.get(buffers[5]);
                    String mJenisApkOnline = buffers[6];
                    dataSet.add(new Mahasiswa(mNama, mGender, mKotaAsal, mFrekuensiAkses, mJurusan, mHobi, mJenisApkOnline));
                }
                DefaultTableModel dtm2 = (DefaultTableModel) dataSetTable.getModel();
                Object rowData[] = new Object[7];
                for(int i=0;i<dataSet.size();i++){
                    rowData[0] = dataSet.get(i).getNama();
                    rowData[1] = dataSet.get(i).getGender();
                    rowData[2] = dataSet.get(i).getJurusan();
                    rowData[3] = dataSet.get(i).getHobi();
                    rowData[4] = dataSet.get(i).getKotaAsal();
                    rowData[5] = dataSet.get(i).getFrekuensiAkses();
                    rowData[6] = "";
                    dtm2.addRow(rowData);
                }
            } catch(IOException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_openDataSetButtonMousePressed

    private void clusterDatasetButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clusterDatasetButtonMousePressed
        som.setDataSet(dataSet);
        ArrayList<Integer> clusters = som.clusterDataSet();
        DefaultTableModel dtm2 = (DefaultTableModel) dataSetTable.getModel();
        int rowCount = dtm2.getRowCount();
        for(int i=rowCount-1;i>=0;i--){
            dtm2.removeRow(i);
        }
        Object rowData[] = new Object[7];
        for(int i=0;i<dataSet.size();i++){
            rowData[0] = dataSet.get(i).getNama();
            rowData[1] = dataSet.get(i).getGender();
            rowData[2] = dataSet.get(i).getJurusan();
            rowData[3] = dataSet.get(i).getHobi();
            rowData[4] = dataSet.get(i).getKotaAsal();
            rowData[5] = dataSet.get(i).getFrekuensiAkses();
            if(clusters.get(i)==0){
                rowData[6] = "e-commerce";
            }
            else if(clusters.get(i)==1){
                rowData[6] = "social media";
            }
            else if(clusters.get(i)==2){
                rowData[6] = "entertainment";
            }
            else if(clusters.get(i)==3){
                rowData[6] = "education";
            }
            dtm2.addRow(rowData);
        }
    }//GEN-LAST:event_clusterDatasetButtonMousePressed

