    private void trainTheSOMMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_trainTheSOMMousePressed
        Integer maxEphoc = Integer.parseInt(this.maxEphoc.getText());
        Double learningRate = Double.parseDouble(this.learningRate.getText());
        this.som = new SOM(this.trainingSet, learningRate, maxEphoc);
        DefaultTableModel dtm = (DefaultTableModel) weightMatrix.getModel();
        Object rowData[] = new Object[this.som.getNumInputNode()];
        for(int i=0;i<this.som.getNumInputNode();i++){
            if(i==0){
                rowData[0] = "Gender";
            }
            else if(i==1){
                rowData[0] = "Jurusan";
            }
            else if(i==2){
                rowData[0] = "Hobi";
            }
            else if(i==3){
                rowData[0] = "Kota Asal";
            }
            else if(i==4){
                rowData[0] = "Frekuensi Mengakses Internet";
            }
            rowData[1] = this.som.getWeightMatrix()[i][0];
            rowData[2] = this.som.getWeightMatrix()[i][1];
            rowData[3] = this.som.getWeightMatrix()[i][2];
            rowData[4] = this.som.getWeightMatrix()[i][3];
            dtm.addRow(rowData);
        }
    }//GEN-LAST:event_trainTheSOMMousePressed

